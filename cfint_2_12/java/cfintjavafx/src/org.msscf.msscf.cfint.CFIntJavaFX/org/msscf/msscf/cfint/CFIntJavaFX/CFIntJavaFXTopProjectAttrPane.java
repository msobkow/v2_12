		ICFIntTopProjectObj eff = (ICFIntTopProjectObj)getJavaFXFocus();
		if( eff != null ) {
			if( null != eff.getEdit() ) {
				eff = (ICFIntTopProjectObj)eff.getEdit();
			}
		}
		return( eff );
