/**
 *	CFSecJavaFXSecGrpMembEltTabPane JavaFX Element TabPane implementation
 *	for SecGrpMemb.
 */
public class CFSecJavaFXSecGrpMembEltTabPane
extends CFTabPane
implements ICFSecJavaFXSecGrpMembPaneCommon
{
	protected ICFFormManager cfFormManager = null;
	protected ICFSecJavaFXSchema javafxSchema = null;
	protected boolean javafxIsInitializing = true;

	public CFSecJavaFXSecGrpMembEltTabPane( ICFFormManager formManager, ICFSecJavaFXSchema argSchema, ICFSecSecGrpMembObj argFocus ) {
		super();
		final String S_ProcName = "construct-schema-focus";
		if( formManager == null ) {
			throw new CFLibNullArgumentException( getClass(),
				S_ProcName,
				1,
				"formManager" );
		}
		cfFormManager = formManager;
		if( argSchema == null ) {
			throw new CFLibNullArgumentException( getClass(),
				S_ProcName,
				2,
				"argSchema" );
		}
		// argFocus is optional; focus may be set later during execution as
		// conditions of the runtime change.
		javafxSchema = argSchema;
		setJavaFXFocusAsSecGrpMemb( argFocus );
		// Wire the newly constructed Panes/Tabs to this TabPane
		javafxIsInitializing = false;
	}

	public ICFFormManager getCFFormManager() {
		return( cfFormManager );
	}

	public void setCFFormManager( ICFFormManager value ) {
		final String S_ProcName = "setCFFormManager";
		if( value == null ) {
			throw new CFLibNullArgumentException( getClass(),
				S_ProcName,
				1,
				"value" );
		}
		cfFormManager = value;
	}

	public ICFSecJavaFXSchema getJavaFXSchema() {
		return( javafxSchema );
	}

	public void setJavaFXFocus( ICFLibAnyObj value ) {
		final String S_ProcName = "setJavaFXFocus";
		if( ( value == null ) || ( value instanceof ICFSecSecGrpMembObj ) ) {
			super.setJavaFXFocus( value );
		}
		else {
			throw new CFLibUnsupportedClassException( getClass(),
				S_ProcName,
				"value",
				value,
				"ICFSecSecGrpMembObj" );
		}
	}

	public void setJavaFXFocusAsSecGrpMemb( ICFSecSecGrpMembObj value ) {
		setJavaFXFocus( value );
	}

	public ICFSecSecGrpMembObj getJavaFXFocusAsSecGrpMemb() {
		return( (ICFSecSecGrpMembObj)getJavaFXFocus() );
	}

	public void setPaneMode( CFPane.PaneMode value ) {
		CFPane.PaneMode oldMode = getPaneMode();
		super.setPaneMode( value );
	}
}
